package com.wadejerry.scms.webservice.server.support.pfm.dto;

import java.util.Date;

public class CarTypeDto {
	private int carTypeId;	//车辆类型Id
	private String typeCode; //车辆类型编号
	private String typeName; //车辆类型名称
	private int inId; //上级类型Id
	private String note; //备注信息
	private int status;  //状态
	private long updateTime; //更新时间
	private int carFull;
	private int maxstop;
	
	public int getCarTypeId() {
		return carTypeId;
	}
	public void setCarTypeId(int carTypeId) {
		this.carTypeId = carTypeId;
	}
	public String getTypeCode() {
		return typeCode;
	}
	public void setTypeCode(String typeCode) {
		this.typeCode = typeCode;
	}
	public String getTypeName() {
		return typeName;
	}
	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}
	public int getInId() {
		return inId;
	}
	public void setInId(int inId) {
		this.inId = inId;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public long getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime.getTime();
	}
	public int getCarFull() {
		return carFull;
	}
	public void setCarFull(int carFull) {
		this.carFull = carFull;
	}
	public int getMaxstop() {
		return maxstop;
	}
	public void setMaxstop(int maxstop) {
		this.maxstop = maxstop;
	}
}
